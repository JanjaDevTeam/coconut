<div class="enviar-projeto bbox">
<h4>E é só enviar meu projeto e ele aparecerá no site?</h4>
<p>Na verdade, não. Nós <strong>selecionamos os projetos</strong> que irão aparecer na na Solucionática Projetos. 
	Chamamos este processo de curadoria: nosso objetivo é divulgar apenas projetos que tenham 
	bom potencial de provocar mudanças sociais e ambientais positivas e que estejam prontos para uma 
	ampla campanha de captação de recursos. </p>

<h4>E que tipos de projetos a Solucionática Projetos aceita?</h4>
<p>Nós só aceitamos projetos que se proponham a eliminar ou reduzir [1] uso insustentável dos recursos 
	naturais e [2] condições de vida degradantes ou limitantes do pleno 
	desenvolvimento das capacidades intelectuais e motoras do ser humando. </p>

<p><strong>Não financiamos empresas ou programas</strong>, apenas projetos. Se você precisa capital para começar uma empresa ou se precisa levantar fundos para manter um programa, nenhum problema, 
	mas essas modalidades estão fora do escopo da a Solucionática Projetos. Nós só aceitaremos projetos, 
	ou seja: algo que seja finito, com um começo e um fim bem delimitados. 
	Por exemplo, se você quer ampliar sua estrutura de alfabetização de adultos em uma comunidade carente, 
	ótimo, a a Solucionática Projetos é o lugar. Mas a a Solucionática Projetos não é o lugar certo se você 
	precisar de recursos para manter essa estrutura por tempo indeterminado. Um curta-metragem sobre reciclagem 
	em grandes centros urbanos, sim; manter uma escola de cinema, sem um projeto com prazo definido, não. </p>

<p> Todos os projetos têm de oferecer contra-partidas. A Solucionática Projetos é uma maneira diferente 
	de engajar pessoas interessadas em causas sociais e ambientais. 
	Se você não acha que as contra-partidas sejam parte fundamental do seu projeto, talvez a a 
	Solucionática Projetos não seja a melhor solução para você. Lembre-se que rifas, sorteios, 
	retorno financeiro e participação societária são proibidos.</p>

<p>Qualquer dúvida que você tiver, é só falar com a gente por aqui.</p>
